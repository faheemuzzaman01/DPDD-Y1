def binarySearch(list, target, left, right):
    if left > right:
        return -1
    mid = (left + right) // 2
    if list[mid] == target:
        return mid
    elif list[mid] > target:
        return binarySearch(list, target, left, mid - 1)
    else:
        return binarySearch(list, target, mid + 1, right)

